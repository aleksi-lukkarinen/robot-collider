namespace ColliderApp.Common.Utils;


/// <summary>
/// Enum <see cref="Direction"/> models the possibility to chooce
/// one of four directions: up, right, down, and left.
/// </summary>
public enum Direction {
    /// <summary>Represents upwards direction.</summary>
    Up = 0,

    /// <summary>Represents rightwards direction.</summary>
    Right = 1,

    /// <summary>Represents downwards direction.</summary>
    Down = 2,

    /// <summary>Represents leftwards direction.</summary>
    Left = 3,
}
